import Foundation

/// This enumeration can be used to describe Rule styles.
public enum Prestyle {
    /// Bold text
    case bold
    /// Italic text
    case italic
    /// Bold and Italic
    case boldItalic
    /// Strikethrough text
    case strikethrough
    /// Underline text
    case underline
}
